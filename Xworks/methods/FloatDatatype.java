class FloatDatatype
{
	public static void main(String[] args)
	{
		float itemPrice = 19.99f;
		float piValue = 3.14f;
		float distanceInKilometers = 10.5f;
		float weightInPounds = 150.5f;
		float heightInMeters = 1.75f;
		float averageScore = 85.5f;
		float percentageComplete = 75.2f;
		float temperatureInFahrenheit = 68.4f;
		float speedInKmph = 60.0f;
		float volumeInLiters = 2.5f;

		System.out.println("Item Price: " + itemPrice);
		System.out.println("Value of Pi: " + piValue);
		System.out.println("Distance in Kilometers: " + distanceInKilometers);
		System.out.println("Weight in Pounds: " + weightInPounds);
		System.out.println("Height in Meters: " + heightInMeters);
		System.out.println("Average Score: " + averageScore);
		System.out.println("Percentage Complete: " + percentageComplete);
		System.out.println("Temperature in Fahrenheit: " + temperatureInFahrenheit);
		System.out.println("Speed in Kmph: " + speedInKmph);
		System.out.println("Volume in Liters: " + volumeInLiters);

	}
}