class LongDatatype
{
	public static void main(String[] args)
	{
		long distanceToSun = 149600000000L;
		long worldPopulation = 7800000000L;
		long nationalDebt = 22000000000000L;
		long bytesOfMemory = 8589934592L;
		long secondsInYear = 31536000L;
		long speedOfLightInVacuum = 299792458L;
		long numberOfStarsInMilkyWay = 100000000000L;
		long numberOfCellsInHumanBody = 37000000000000L;
		long distanceToNearestStar = 9460730472580800L;
		long nationalGDP = 2100000000000L;

		System.out.println("Distance to Sun: " + distanceToSun);
		System.out.println("World Population: " + worldPopulation);
		System.out.println("National Debt: " + nationalDebt);
		System.out.println("Bytes of Memory: " + bytesOfMemory);
		System.out.println("Seconds in a Year: " + secondsInYear);
		System.out.println("Speed of Light in Vacuum: " + speedOfLightInVacuum);
		System.out.println("Number of Stars in Milky Way: " + numberOfStarsInMilkyWay);
		System.out.println("Number of Cells in Human Body: " + numberOfCellsInHumanBody);
		System.out.println("Distance to Nearest Star: " + distanceToNearestStar);
		System.out.println("National GDP: " + nationalGDP);
		
		
	}
}