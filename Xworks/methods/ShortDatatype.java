class ShortDatatype
{
	public static void main(String[] args)
	{
		short maxShortValue = 32767;
		short minShortValue = -32768;
		short populationInThousands = 15000;
		short distanceInMeters = 5000;
		short temperatureInKelvin = 273;
		short numberOfFloors = 10;
		short annualRainfallInMm = 1200;
		short monthlyIncome = 2500;
		short numberOfEmployees = 300;
		short numberOfChapters = 12;

		System.out.println("Max Short Value: " + maxShortValue);
		System.out.println("Min Short Value: " + minShortValue);
		System.out.println("Population in Thousands: " + populationInThousands);
		System.out.println("Distance in Meters: " + distanceInMeters);
		System.out.println("Temperature in Kelvin: " + temperatureInKelvin);
		System.out.println("Number of Floors: " + numberOfFloors);
		System.out.println("Annual Rainfall in mm: " + annualRainfallInMm);
		System.out.println("Monthly Income: " + monthlyIncome);
		System.out.println("Number of Employees: " + numberOfEmployees);
		System.out.println("Number of Chapters: " + numberOfChapters);
	}
}