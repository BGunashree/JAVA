class DoubleDatatype
{
	public static void main(String[] args)
	{
		double bankBalance = 1000.75;
		double circleArea = 78.54;
		double gravitationalConstant = 9.81;
		double averageTemperature = 36.6;
		double priceOfGold = 1500.55;
		double salaryPerMonth = 4500.30;
		double distanceToMoon = 384400.0;
		double piDouble = 3.141592653589793;
		double exchangeRate = 1.12;
		double speedOfLight = 299792458.0;

		System.out.println("Bank Balance: " + bankBalance);
		System.out.println("Circle Area: " + circleArea);
		System.out.println("Gravitational Constant: " + gravitationalConstant);
		System.out.println("Average Temperature: " + averageTemperature);
		System.out.println("Price of Gold: " + priceOfGold);
		System.out.println("Salary Per Month: " + salaryPerMonth);
		System.out.println("Distance to Moon: " + distanceToMoon);
		System.out.println("Value of Pi (Double): " + piDouble);
		System.out.println("Exchange Rate: " + exchangeRate);
		System.out.println("Speed of Light: " + speedOfLight);
	}
}